.INCLUDE "m32def.inc"

.ORG 0x00
MAIN:
SBI DDRD,PD5
SBI DDRD,PD4

LDI R16,0x00
OUT TCNT1H,R16
OUT TCNT1L,R16
LDI R16,HIGH(0x26)
OUT OCR1AH,R16
LDI R16,LOW(0x26)
OUT OCR1AL,R16
LDI R16,HIGH(0x4B)
OUT OCR1BH,R16
LDI R16,LOW(0x4B)
OUT OCR1BL,R16
LDI R16,HIGH(0x7D)
OUT ICR1H,R16
LDI R16,LOW(0x7D)
OUT ICR1L,R16
LDI R16,0xA2
OUT TCCR1A,R16
LDI R16,0x14
OUT TCCR1B,R16

LOOP: RJMP LOOP
