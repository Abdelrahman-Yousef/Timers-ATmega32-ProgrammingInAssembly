.INCLUDE "m32def.inc"

.ORG 0x00
MAIN:
LDI R16,0x00
OUT TCNT1H,R16
OUT TCNT1L,R16

LDI R16,HIGH(0x200)
OUT OCR1AH,R16
LDI R16,LOW(0x200)
OUT OCR1AL,R16

LDI R16,HIGH(0x10)
OUT OCR1BH,R16
LDI R16,LOW(0x10)
OUT OCR1BL,R16

LDI R16,0x50
OUT TCCR1A,R16
LDI R16,0x09
OUT TCCR1B,R16

SBI DDRD,PD4
SBI DDRD,PD5

LOOP: RJMP LOOP